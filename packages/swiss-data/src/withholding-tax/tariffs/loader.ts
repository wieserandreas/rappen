/**
 * All cantons for which official ESTV tariffs are loaded.
 * Currently: all 26 Swiss cantons.
 */
export const SUPPORTED_QST_CANTONS: ReadonlyArray<CantonCode> = CANTONS;

/**
 * Returns true if withholding tax tariffs are loaded for this canton.
 */
export function isQstCantonSupported(canton: CantonCode): boolean {
	return SUPPORTED_QST_CANTONS.includes(canton);
}

/**
 * Look up a single tariff series by canton, group, child count, and church.
 *
 * Handles canton-specific exceptions per ESTV spec section 1.2 / Abacus doc:
 * - GE/NE/VD/VS/TI: only "ohne Kirchensteuer" (church=false). Y is mapped to N.
 * - JU: only "mit Kirchensteuer" (church=true). N is mapped to Y.
 *
 * Returns null if no matching tariff is found.
 *
 * @param year - Tax year. Defaults to the latest available year ≤ today.
 */
export function getTariff(
	canton: CantonCode,
	group: TariffCode,
	children: number,
	church: boolean,
	year?: number,
): CompactTariffSeries | null {
	const targetYear = year ?? getCurrentQstYear();
	const available = listAvailableQstYears();
	if (!available.includes(targetYear)) {
		throw new Error(
			`Year ${targetYear} not available. Available years: ${available.join(", ") || "(none)"}.`,
		);
	}

	const data = loadCanton(canton, targetYear);

	// Apply canton-specific church-tax rules
	const NO_CHURCH_CANTONS = new Set<CantonCode>(["GE", "NE", "VD", "VS", "TI"]);
	const ONLY_CHURCH_CANTONS = new Set<CantonCode>(["JU"]);

	let effectiveChurch = church;
	if (NO_CHURCH_CANTONS.has(canton)) effectiveChurch = false;
	if (ONLY_CHURCH_CANTONS.has(canton)) effectiveChurch = true;

	// Tariff code H requires at least 1 child
	if (group === "H" && children < 1) {
		return null;
	}

	const code = buildEstvCode(group, children, effectiveChurch);
	return data.tariffs[code] ?? null;
}

/**
 * Get the canton's median monthly salary (used for tariff C calculation).
 */
export function getMedianSalary(canton: CantonCode, year?: number): number | null {
	return loadCanton(canton, year ?? getCurrentQstYear()).median_salary;
}

/**
 * Get all available tariff codes for a canton (debug/validation).
 */
export function getAvailableTariffsForCanton(canton: CantonCode, year?: number): string[] {
	return Object.keys(loadCanton(canton, year ?? getCurrentQstYear()).tariffs).sort();
}

/**
 * Get the data file metadata (created_at, source) for a canton.
 */
export function getCantonDataMeta(
	canton: CantonCode,
	year?: number,
): {
	year: number;
	created_at: string;
	source: string;
	median_salary: number | null;
	tariff_count: number;
} {
	const targetYear = year ?? getCurrentQstYear();
	const data = loadCanton(canton, targetYear);
	return {
		year: targetYear,
		created_at: data.created_at,
		source: data.source,
		median_salary: data.median_salary,
		tariff_count: Object.keys(data.tariffs).length,
	};
}

/**
 * Backwards-compat alias used by older tests.
 */
export function getAvailableTariffs(): string[] {
	const all: string[] = [];
	for (const year of listAvailableQstYears()) {
		for (const canton of CANTONS) {
			try {
				for (const code of getAvailableTariffsForCanton(canton, year)) {
					all.push(`${year}_${canton}_${code}`);
				}
			} catch {
				// Canton not yet available
			}
		}
	}
	return all;
}

/**
 * Reset the in-memory cache. Used by tests.
 */
export function _resetQstCache() {
	cache.clear();
	cachedDataRoot = null;
	cachedAvailableYears = null;
}
